                    <p>In an era of rapid climate change, 93% of greenhouse gas emissions are absorbed back into the oceans. The ocean temperatures are rapidly increasing. This means that the habits that sharks are living in are also changing. Sharks are moving away from the equator towards cooler waters. The reason that warming waters are an evolutionary issue for sharks is that sharks take a long time to adapt. Some sharks have significantly long lifespans, meaning that it will be difficult for those species to rapid adapt and evolve to the warming oceans. If the temperatures of the oceans keep rising, the sharks may not be able to adapt fast enough, and populations could be facing extinction. Source: <cite><a href="https://www.climatecentral.org/news/climate-change-sharks-19221" target="_blank">Climate Center</a></cite></p>
